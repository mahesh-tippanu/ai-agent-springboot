package com.dravyn.aiagent.model;

public class AgentTaskRequest {
    private String task;
    private String input;

    // Getters and Setters
    public String getTask() {
        return task;
    }

    public void setTask(String task) {
        this.task = task;
    }

    public String getInput() {
        return input;
    }

    public void setInput(String input) {
        this.input = input;
    }
}
